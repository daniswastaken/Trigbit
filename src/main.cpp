#include <Arduino.h>
#include <Wire.h>
#include <Adafruit_GFX.h>
#include <Adafruit_SSD1306.h>

#include "led.h"

#define OLED_ADDR 0x3C
Adafruit_SSD1306 lcd(128, 64, &Wire, -1);

int redButton = 27;
int blueButton = 26;
int whiteButton = 25;

bool lastAnyPressed = false;

void test()
{
  bool redState = (digitalRead(redButton) == LOW);
  bool blueState = (digitalRead(blueButton) == LOW);
  bool whiteState = (digitalRead(whiteButton) == LOW);

  if (redState) {
    falseLed();
    redState = !redState;
    delay(150);
  }

  if (blueState) {
    trueLed();
    blueState = !blueState;
    delay(150);
  }
}

void setup()
{
  // 3 button, left to right
  pinMode(redButton, INPUT_PULLUP);
  pinMode(blueButton, INPUT_PULLUP);
  pinMode(whiteButton, INPUT_PULLUP);

  initLed();

  lcd.begin(SSD1306_SWITCHCAPVCC, OLED_ADDR);
  lcd.clearDisplay();
  lcd.display();
}

void loop()
{
  int redState = digitalRead(redButton);
  int blueState = digitalRead(blueButton);
  int whiteState = digitalRead(whiteButton);

  test();
}

